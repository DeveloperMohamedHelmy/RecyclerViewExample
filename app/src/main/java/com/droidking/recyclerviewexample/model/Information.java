public class Information {


    protected int GROUP_FULL_ICON_ID;
    protected String GROUP_NAME;
    protected String GROUP_NOTES;





    public String getGROUP_NAME() {
        return GROUP_NAME;
    }

    public void setGROUP_NAME(String GROUP_NAME) {
        this.GROUP_NAME = GROUP_NAME;
    }

    public int getGROUP_FULL_ICON_ID() {
        return GROUP_FULL_ICON_ID;
    }

    public void setGROUP_FULL_ICON_ID(int GROUP_FULL_ICON_ID) {
        this.GROUP_FULL_ICON_ID = GROUP_FULL_ICON_ID;
    }

    public String getGROUP_NOTES() {
        return GROUP_NOTES;
    }

    public void setGROUP_NOTES(String GROUP_NOTES) {
        this.GROUP_NOTES = GROUP_NOTES;
    }

}
